package com.example;

public class Identifier {
    public static void main(String[] args) {
        int a=10;
        System.out.println(a);
    }
}
/*
In the above java code, we have 5 identifiers:


Identifier : class name.
main : method name.
String : predefined class name.
args : variable name.
a :  variable name.
 */